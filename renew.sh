echo -e ""
echo -e "========================================" | lolcat
else
clear
echo -e ""
echo -e "======================================" | lolcat
echo -e ""
echo -e "        Username Doesnt Exist        "
echo -e ""
echo -e "======================================" | lolcat
fi
